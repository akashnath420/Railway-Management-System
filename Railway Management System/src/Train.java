public class Train {
    int number;
    String name;
    String source;
    String destination;
    float charge;
    String departureTime;

    Train(int number, String name, String source, String destination, float charge, String departureTime) {
        this.number = number;
        this.name = name;
        this.source = source;
        this.destination = destination;
        this.charge = charge;
        this.departureTime = departureTime;
    }
}
