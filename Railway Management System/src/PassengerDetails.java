public class PassengerDetails {
    String name;
    int trainNum;
    int numOfSeats;
    float charges;

    PassengerDetails(String name, int trainNum, int numOfSeats, float charges) {
        this.name = name;
        this.trainNum = trainNum;
        this.numOfSeats = numOfSeats;
        this.charges = charges;
    }
}
